package io.github.aplaraujo.exercicio_4_spring.dto;

public class SumRequest {
    private int a;
    private int b;

    public SumRequest(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }
}
