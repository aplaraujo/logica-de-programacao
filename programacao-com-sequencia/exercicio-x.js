/*
Elaborar um programa que leia uma medida em pés, calcular, armazenar e 
apresentar o seu valor convertido em metros, lembrando que um pé mede 0.3048 metro, 
ou seja, um pé é igual a 30.48 centímetros.
*/

const feetToMeters = feet => feet * 0.3048

console.log(feetToMeters(1))
console.log(feetToMeters(2))
console.log(feetToMeters(3))
console.log(feetToMeters(4))
console.log(feetToMeters(5))